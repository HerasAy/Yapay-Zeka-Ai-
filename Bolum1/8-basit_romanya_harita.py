class RouteProblem(Problem):
    def actions(self, state):
        # O şehirden gidilebilecek komşuları döndür
        return list(romania_map.get(state, {}).keys())

    def result(self, state, action):
        # Basit graf problemlerinde aksiyon gidilecek şehirdir
        return action
